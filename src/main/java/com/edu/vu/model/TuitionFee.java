package com.edu.vu.model;

import java.util.ArrayList;

public class TuitionFee {
	private int grades;
	private String fee;
	private String miscFee;
	private String tuitionFeeRemark;
	private String payDueDate;
	public int getGrades() {
		return grades;
	}
	public void setGrades(int grades) {
		this.grades = grades;
	}
	public String getFee() {
		return fee;
	}
	public void setFee(String fee) {
		this.fee = fee;
	}
	public String getMiscFee() {
		return miscFee;
	}
	public void setMiscFee(String miscFee) {
		this.miscFee = miscFee;
	}
	public String getTuitionFeeRemark() {
		return tuitionFeeRemark;
	}
	public void setTuitionFeeRemark(String tuitionFeeRemark) {
		this.tuitionFeeRemark = tuitionFeeRemark;
	}
	public String getPayDueDate() {
		return payDueDate;
	}
	public void setPayDueDate(String payDueDate) {
		this.payDueDate = payDueDate;
	}
	
	

}
