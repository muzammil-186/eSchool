package com.edu.vu.model;

import java.util.ArrayList;

public class AdmissionCriteria {
	private int classLevel;
	private String age;
	private String eligibleCriteria;
	private String parentNic;
	
	public int getClassLevel() {
		return classLevel;
	}
	public void setClassLevel(int classLevel) {
		this.classLevel = classLevel;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getEligibleCriteria() {
		return eligibleCriteria;
	}
	public void setEligibleCriteria(String eligibleCriteria) {
		this.eligibleCriteria = eligibleCriteria;
	}
	public String getParentNic() {
		return parentNic;
	}
	public void setParentNic(String parentNic) {
		this.parentNic = parentNic;
	}
	

}
